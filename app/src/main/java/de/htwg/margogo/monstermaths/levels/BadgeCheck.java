package de.htwg.margogo.monstermaths.levels;

public class BadgeCheck {

    int gold;
    int silver;

    public BadgeCheck (int gold, int silver) {
        this.gold = gold;
        this.silver = silver;
    }

    public int getGold() {
        return gold;
    }

    public float getSilver() {
        return silver;
    }

}
